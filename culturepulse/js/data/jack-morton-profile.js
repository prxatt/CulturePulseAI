/**
 * Get data collection strategy for specific client
 * @param {string} clientName - Name of Jack Morton client
 * @returns {Object} Customized data collection strategy
 */
export function getClientStrategy(clientName) {
  const keywords = CLIENT_KEYWORDS[clientName.toLowerCase()];
  
  if (!keywords) {
    return {
      keywords: [],
      subreddits: [],
      platforms: ['Reddit', 'LinkedIn', 'Twitter/X'],
      message: 'No specific strategy found for client'
    };
  }
  
  return {
    keywords,
    subreddits: JACK_MORTON_PROFILE.dataCollectionStrategy.sourcePriorities[0].subreddits,
    platforms: ['Reddit', 'LinkedIn', 'Twitter/X', 'Industry forums'],
    description: `Data collection strategy optimized for ${clientName}`,
    priority: 'high'
  };
}

/**
 * Check if trend is relevant to Jack Morton clients
 * @param {Object} trend - Trend object to check
 * @returns {boolean} Whether trend is client-relevant
 */
export function isClientRelevant(trend) {
  const allKeywords = Object.values(CLIENT_KEYWORDS).flat();
  const trendText = `${trend.title} ${trend.shortDescription}`.toLowerCase();
  
  return allKeywords.some(keyword => trendText.includes(keyword.toLowerCase()));
}

